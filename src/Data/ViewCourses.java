    public ViewCourses() {
        initComponents();
        
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        DBManager dbm = new DBManager();
        boolean valid = true;
        
        Course c;
        
        while(valid==true){
            c = dbm.getNextCourse();
            if(c == null){
                valid = false;
            }
            else{
                int[] days = c.getDays();
                String day = "";
                for(int i = 0; i<days.length; i++){
                    if(days[i]==1){
                        switch(i){
                            case 0:
                                day += "M ";
                                break;
                            case 1:
                                day += "T ";
                                break;
                            case 2:
                                day += "W ";
                                break;
                            case 3:
                                day += "Th ";
                                break;
                            case 4:
                                day += "F ";
                        }
                    }
                }
                String type;
                if(c.getType().equals("0"))
                    type = "Standard Class";
                else if(c.getType().equals("1"))
                    type = "Lecture Hall";
                else
                    type = "Laboratory";
                        
                Object[] row = {c.getID(), c.getCourseName(), c.getCourseNumber(), type, c.getCapacity(), day};
                model.addRow(row);
            }
        }
    }

